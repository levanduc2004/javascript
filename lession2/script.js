// Primitive Data Types

// number 
let number = 10;
let number2 = 3.14;
console.log(typeof number, typeof number2);

// String
let string1 = "Hello";
let string2 = `Hello`;
let string3 = 'hello';

console.log(typeof string1, typeof string2, typeof string3)

// Boolean
const isChecked = true;
console.log(typeof isChecked)

// Undefined
let check;
console.log(check)

// null
let testNull = null;
console.log(testNull)

